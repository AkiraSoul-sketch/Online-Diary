/**
 * AuthenticationInformation содержит учётные данные пользователя, используемые при
 * аутентификации на сервисе.
 *
 * Поля:
 * - login: идентификатор пользователя (например, имя пользователя или email).
 * - password: секрет (пароль). Никогда не логируйте и не храните пароль в виде
 *   открытого текста. Используйте безопасные каналы передачи (TLS) и безопасное
 *   хранение (шифрование, защищённые хранилища).
 */
export type AuthenticationInformation = {
  /** Идентификатор пользователя (например, имя пользователя или email). */
  login: string;
  /** Пароль пользователя. Обращайтесь с ним осторожно: не логировать, не встраивать в URL. */
  password: string;
};
